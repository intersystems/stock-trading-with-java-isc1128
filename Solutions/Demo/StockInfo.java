package Demo;

public class StockInfo {
	//properties
		public String name;
		public String mission;
		public String founder;
		
	// no arg constructor
		public StockInfo() {}
	
	//arg constructor
		public StockInfo(String name, String mission, String founder) {
			this.name = name;
			this.mission = mission;
			this.founder = founder;
		}
	
}
